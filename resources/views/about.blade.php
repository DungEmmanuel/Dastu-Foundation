                      According to the Substance Abuse and Mental Health Services Administration’s (SAMHSA’s), March 2011, National Survey on Drug Use and Health, 23.5 million persons aged 12 or older needed treatment for an illicit drug or alcohol abuse problem in 2009 (9.3 percent of persons aged 12 or older). Of these, only 2.6 million—11.2 percent of those who needed treatment—received it at a specialty facility.

                      SAMHSA also reports characteristics of admissions and discharges from substance abuse treatment facilities* in its Treatment Episode Data Set2 (TEDS). 

                      According to TEDS, there were 1.8 million admissions in 2008 for treatment of alcohol and drug abuse to facilities that report to State administrative data systems. Most treatment admissions (41.4 percent) involved alcohol abuse. Heroin and other opiates accounted for the largest percentage of drug-related admissions (20.0 percent), followed by marijuana (17.0 percent).

                      In 2013 alone, a total of 3,271 drug dependent persons were successfully counseled in National Drug Law Enforcement Agency (NDLEA) facilities nationwide. This comprises of 3,062 males and 209 females. Additional 802 treated cases were reported by hospitals and other drug dependence treatment centers in Nigeria.(NDLEA, 2013).

